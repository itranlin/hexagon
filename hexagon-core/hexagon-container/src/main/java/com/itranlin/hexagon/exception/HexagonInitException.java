package com.itranlin.hexagon.exception;

/**
 * The type Hexagon init exception.
 */
public class HexagonInitException extends RuntimeException {

    /**
     * Instantiates a new Hexagon init exception.
     *
     * @param message the message
     */
    public HexagonInitException(String message) {
        super(message);
    }

    /**
     * Instantiates a new Hexagon init exception.
     *
     * @param throwable the throwable
     */
    public HexagonInitException(Throwable throwable) {
        super(throwable);
    }

}
